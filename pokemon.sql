--Regiones
create table regions(
  id serial primary key,
  name text not null
);

--UNO A UNO
--Centros Pokemon
create table centers(
  id serial primary key,
  name text not null,
  region_id int not null,
  available boolean default '1',
  foreign key (region_id) references regions(id)
);
--Catalogo Tipos
create table types(
  id serial primary key,
  name text not null
);
--Catalogo Habilidades
create table powers(
  id serial primary key,
  name text not null
);
--Uno a muchos
--Catalogo Pokemon
create table pokemons(
  id serial primary key,
  species text not null,
  image text not null,
  region_id int not null,
  hit_points int not null,
  attack int not null,
  defense int not null,
  speed int not null,
  evasion int not null,
  accuracy int not null,
  url text not null,
  foreign key (region_id) references regions(id)
);
--Catalgo estatus
create table statuses(
  id serial primary key,
  name text not null,
  time int not null,
  disappears int not null
);
--MUCHOS A MUCHOS
--Habilidades
create table pokemon_power(
  id serial primary key,
  pokemon_id int not null,
  power_id int not null,
  foreign key (pokemon_id) references pokemons(id),
  foreign key (power_id) references powers(id)
);
--MUCHO A MUCHOS
--Tipos
create table pokemon_type(
  id serial primary key,
  pokemon_id int not null,
  type_id int not null,
  foreign key (pokemon_id) references pokemons(id),
  foreign key (type_id) references types(id)
);
--UNO A UNO
create table evolutions(
  id serial primary key,
  pokemon_id int not null,
  pokemon_id_e int not null,
  foreign key (pokemon_id) references pokemons(id),
  foreign key (pokemon_id_e) references pokemons(id)
);

--Uno a muchos
create table trainers(
  id serial primary key,
  name text not null,
  last_name text not null,
  image text not null,
  username text not null,
  password text not null,
  birthday date not null,
  region_id int not null,
  gender text default 'Hombre',
  leader boolean default '1',
  region_id_actual int not null,
  available boolean default '1',
  foreign key (region_id) references regions(id),
  foreign key (region_id_actual) references regions(id)
);
--UNO A MUCHOS (STATUS)
--UNO A UNO (Pokemon)
--Mis pokemones
create table pokeballs(
  id serial primary key,
  trainer_id int not null,
  pokemon_id int not null,
  url text not null,
  specie text not null,
  image text not null,
  alias text,
  gender text default 'Masculino',
  level int not null,
  hit_points int not null,
  attack int not null,
  defense int not null,
  speed int not null,
  evasion int not null,
  accuracy int not null,
  status_id int not null,
  status text not null,
  available boolean default '1',
  foreign key (pokemon_id) references pokemons(id),
  foreign key (trainer_id) references trainers(id),
  foreign key (status_id) references statuses(id)
);

--Uno a muchos
create table regenerators(
  id serial primary key,
  slots int not null default 50,
  slots_functional int not null,
  maintenance boolean default '0',
  center_id int not null,
  available boolean default '1',
  foreign key (center_id) references centers(id)
);
--Muchos a muchos
create table regenerator_pokeball(
  id serial primary key,
  date_start timestamp,
  date_estimated timestamp,
  date_end timestamp,
  regenerator_id int not null,
  hit_points int not null,
  status int not null,
  pokeball_id int not null,
  available boolean default '1',
  foreign key (regenerator_id) references regenerators(id),
  foreign key (pokeball_id) references pokeballs(id)
);

create table rooms(
  id serial primary key,
  capacity int not null,
  center_id int not null,
  available boolean default '1',
  foreign key (center_id) references centers(id)
);

create table room_trainer(
  id serial primary key,
  in_use boolean default '0',
  room_id int not null,
  trainer_id int not null,
  available boolean default '1',
  foreign key (room_id) references rooms(id),
  foreign key (trainer_id) references trainers(id)
);

